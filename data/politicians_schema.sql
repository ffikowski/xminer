CREATE TABLE IF NOT EXISTS politicians (
    key_column INTEGER PRIMARY KEY,
    id INTEGER,
    geburtsdatum TIMESTAMPTZ,
    geburtsort VARCHAR(25),
    geburtsland VARCHAR(23),
    sterbedatum DOUBLE PRECISION,
    geschlecht VARCHAR(8),
    familienstand VARCHAR(29),
    religion VARCHAR(26),
    beruf VARCHAR(81),
    partei_kurz VARCHAR(21),
    vita_kurz TEXT,
    veroeffentlichungspflichtiges DOUBLE PRECISION,
    nachname VARCHAR(19),
    vorname VARCHAR(17),
    ortszusatz VARCHAR(14),
    adel VARCHAR(5),
    praefix VARCHAR(3),
    anrede_titel VARCHAR(3),
    akad_titel VARCHAR(9),
    historie_von TIMESTAMPTZ,
    historie_bis TIMESTAMPTZ,
    wp_wp INTEGER,
    wp_mdbwp_von TIMESTAMPTZ,
    wp_mdbwp_bis DOUBLE PRECISION,
    wp_wkr_nummer DOUBLE PRECISION,
    wp_wkr_name VARCHAR(61),
    wp_wkr_land VARCHAR(2),
    wp_liste VARCHAR(2),
    wp_mandatsart VARCHAR(11),
    wp_institutionen VARCHAR(10),
    username VARCHAR(15)
);